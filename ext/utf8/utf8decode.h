/* {{{ utf8_decode
 */
static inline uint32_t
utf8_decode(uint32_t *restrict state, uint32_t *restrict codep, uint32_t byte)
{
	uint32_t type = utf8d[byte];

	*codep = (*state != UTF8_ACCEPT) ?
		(byte & 0x3fu) | (*codep << 6) :
		(0xff >> type) & (byte);

	*state = utf8d[256 + *state + type];
	return *state;
}

